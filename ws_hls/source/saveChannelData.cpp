    channeldata_X->m_acqMetric = channel_X.getAcqMetric();
    channeldata_X->m_indexPeak = channel_X.getIndexPeak();
    channeldata_X->m_isTrackingInitialised = channel_X.getIsTrackingInitialised();
    channeldata_X->m_trackRequiredSamples = channel_X.getTrackRequiredSamples();
